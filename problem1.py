#Create a list
#Loop through the list using a for each, 
#for index, for loop value with index
#2 minutes

x = [10, 9, 5, 3, 2]
#for each loop
for val in x:
    print(val)

#loop through the list index
for i in range(len(x)):
    print(i)

#loop through the values of the list
#using the index
for i in range(len(x)):
    print(x[i])